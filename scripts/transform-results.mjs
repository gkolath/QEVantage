/**
 * transform-results.mjs
 * Converts Playwright JSON reporter output → src/data/test-results.ts
 * Run after: npx playwright test --reporter=json
 */

import { readFileSync, writeFileSync, existsSync } from 'fs';
import { resolve, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const resultsPath = resolve(__dirname, '../test-results/results.json');
const outputPath  = resolve(__dirname, '../src/data/test-results.ts');

if (!existsSync(resultsPath)) {
  console.error('❌ test-results/results.json not found. Run: npx playwright test first.');
  process.exit(1);
}

const raw = JSON.parse(readFileSync(resultsPath, 'utf8'));

// Map spec file name → framework label
const FRAMEWORK_MAP = {
  'dashboard':   'Playwright',
  'automation':  'Playwright',
  'performance': 'Playwright',
  'security':    'Playwright',
  'release':     'Playwright',
};

const SUITE_NAME_MAP = {
  'dashboard':   'Dashboard — Release Readiness Score',
  'automation':  'Automation Engine — Test Suite Management',
  'performance': 'Performance Profiling — SLA & Telemetry',
  'security':    'Security & Audit — Findings and Compliance',
  'release':     'Release Management — RRS Engine & Pipeline',
};

const ID_MAP = {
  'dashboard':   'suite-pw-1',
  'automation':  'suite-pw-2',
  'performance': 'suite-pw-3',
  'security':    'suite-pw-4',
  'release':     'suite-pw-5',
};

/** Flatten all specs from a Playwright suite tree */
function flattenSpecs(suites) {
  const specs = [];
  for (const suite of suites) {
    if (suite.specs) specs.push(...suite.specs);
    if (suite.suites) specs.push(...flattenSpecs(suite.suites));
  }
  return specs;
}

const results = [];

for (const suite of raw.suites ?? []) {
  // suite.title is the file path, e.g. "dashboard.spec.ts"
  const fileKey = Object.keys(SUITE_NAME_MAP).find(k => suite.title.includes(k));
  if (!fileKey) continue;

  const specs = flattenSpecs([suite]);

  let passed = 0, failed = 0, flaky = 0;

  for (const spec of specs) {
    const statuses = (spec.tests ?? []).flatMap(t => (t.results ?? []).map(r => r.status));
    const hasPassed  = statuses.includes('passed') || statuses.includes('expected');
    const hasFailed  = statuses.includes('failed')  || statuses.includes('unexpected');
    const isFlaky    = hasPassed && hasFailed;

    if (isFlaky)       flaky++;
    else if (hasPassed) passed++;
    else if (hasFailed) failed++;
  }

  const total = passed + failed + flaky;
  const status = failed > 0 ? 'Failed' : flaky > 0 ? 'Passed' : 'Passed';

  results.push({
    id:        ID_MAP[fileKey],
    name:      SUITE_NAME_MAP[fileKey],
    framework: FRAMEWORK_MAP[fileKey],
    total,
    passed,
    failed,
    flaky,
    status,
  });
}

const timestamp = new Date().toISOString().replace('T', ' ').slice(0, 19);

const tsOutput = `// AUTO-GENERATED by scripts/transform-results.mjs
// Last run: ${timestamp}
// Source: test-results/results.json — do not edit manually

export interface PlaywrightSuiteResult {
  id: string;
  name: string;
  framework: string;
  total: number;
  passed: number;
  failed: number;
  flaky: number;
  status: 'Passed' | 'Failed' | 'Running' | 'Idle';
}

export const PLAYWRIGHT_RESULTS: PlaywrightSuiteResult[] = ${JSON.stringify(results, null, 2)};

export const RESULTS_TIMESTAMP = '${timestamp}';
`;

writeFileSync(outputPath, tsOutput, 'utf8');
console.log(`✅ Wrote ${results.length} suite results → src/data/test-results.ts`);
console.table(results.map(r => ({ suite: r.name.split('—')[0].trim(), total: r.total, passed: r.passed, failed: r.failed, flaky: r.flaky, status: r.status })));
